// Scale on hover animation for cards
interface HoverScaleProps {
  children: ReactNode;
  className?: string;
}

export function HoverScale({ children, className = "" }: HoverScaleProps) {
  const canHover = useHoverCapable();

  return (
    <motion.div
      whileHover={canHover ? { scale: 1.02 } : undefined}
      whileTap={{ scale: 0.98 }}
      transition={{ duration: 0.2 }}
      className={className}
    >
      {children}
    </motion.div>
  );
}

// Fade in from bottom with blur
interface FadeInBlurProps {
  children: ReactNode;
  className?: string;
  delay?: number;
}

export function FadeInBlur({ children, className = "", delay = 0 }: FadeInBlurProps) {
  const ref = useRef(null);
  const isInView = useInView(ref, { once: true, margin: "-100px" });

  return (
    <motion.div
      ref={ref}
      initial={{ opacity: 0, y: 40, filter: "blur(10px)" }}
      animate={
        isInView
          ? { opacity: 1, y: 0, filter: "blur(0px)" }
          : { opacity: 0, y: 40, filter: "blur(10px)" }
      }
      transition={{
        duration: 0.7,
        delay,
        ease: [0.25, 0.1, 0.25, 1],
      }}
      className={className}
    >
      {children}
    </motion.div>
  );
}
